		.ctrl_start(ctrl_start),
    .ctrl_done(ctrl_done),
    .ctrl_idle(ctrl_idle),
    .enable_bypass(enable_bypass),
    .data_mod(data_mod),
	);
	

	// Add user logic here
		
	wire        enable_bypass;
	wire [31:0] data_mod;

	wire [31:0] fifo_out_data;
	wire        fifo_out_valid;
	wire        fifo_out_ready;

	simple_fifo #(
		.DATA_WIDTH(32),
		.DEPTH(1024)
	) fifo (
		.clk(s00_axi_aclk),
		.reset_n(s00_axi_aresetn),
		.in_data(s_axis_tdata),
		.in_valid(s_axis_tvalid),
		.in_ready(s_axis_tready),
		.out_data(fifo_out_data),
		.out_valid(fifo_out_valid),
		.out_ready(m_axis_tready)
	);

	// Post-processing with AXI-Lite regs
	assign m_axis_tdata  = enable_bypass ? s_axis_tdata : (fifo_out_data + data_mod);
	assign m_axis_tvalid = enable_bypass ? s_axis_tvalid : fifo_out_valid;

	// User logic ends

	endmodule
